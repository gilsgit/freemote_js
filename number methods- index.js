// Number methods
const num = 999;

// toString
console.log(num.toString());


// toFixed also converts to a string
console.log(num.toFixed(2));


// Math.min, Math.max, parseInt
console.log(Math.min(12, 222, 4948383, 5));
console.log(Math.max(23, 499, 32));


// Math.floor, Math.ceil
// round down
console.log(Math.floor(999.99));
// rounds up
console.log(Math.ceil(999.99));
// converts to Integer whole number
console.log(parseInt(999.999));